<h1>Updating the layout for tiny devices</h1>
<h2>Objectives</h2>
<ul>
    <li>Make the site useable and still look decent on tiny screens (480px wide and smaller)</li>
    <li>Learn how to use the CSS @media query</li>
</ul>
<h2>Results</h2>
<p>I created a new stylesheet that applies when the screen is 480px and smaller.  Since my CSS is already divided into a layout section and an appearance section,
    I only needed to add rules for the layout section to change on the smaller screens.  The images are shrunk down, the nav area modified to wrap, and the footer to expand
    downwards to accomodate the Site Map being laid out vertically instead of horizontally as it is on the desktop version.
</p>
<p>To see for yourself, visit the site on a small phone.</p>