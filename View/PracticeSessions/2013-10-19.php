<h1 class="text-center">Site Re-styling</h1>
<h2>Objectives</h2>
<ol>
    <li>Get rid of the ugly and non-functional background gradient</li>
    <li>Improve visual design to focus on content</li>
</ol>

<h3>What I changed</h3>
<p>
    The previous style used a gradient to run from my secondar color (an orangish-red, or #7D4F0D) to the primary color, #69B2FB<br/>
    This was not very appealing to the eye, and I could not get the gradient to always run to the bottom of the page without repeating.
</p>
<p>
    The new style uses the softer blue color as the overall background, and gives the content area a flat white background.
    The content area's border is 4 pixels wide using a tint of the backgound blue, giving a pleasant transition to the content area from the background.
</p>
<h3>Visual effect</h3>
<p>
    The mono-tone background of a pleasant color provides a workable background that puts attention on the content instead of distracting from it.
</p>