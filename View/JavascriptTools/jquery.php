<h1>What is jQuery</h1>
<p>jQuery is a javascript library. That means that it is written in 100% javascript.
    It makes doing awesome things on your website with Javascript easier, faster, and cross-browser compatible.
</p>
<h1>What can I do with it?</h1>
<ul>
    <li>
        DOM traversal
        <p>Find and manipulate elements of your web page such as <?=htmlspecialchars('<span>, <div>, <nav>, <body>')?>, etc elements</p>
    </li>
    <li>
        Events
        <p>jQuery extends the native javascript element handling features and gives you more power and flexibility to handle user interaction and ways to keep extensive code cohesive</p>
    </li>
    <li>
        Ajax
        <p>Easy helper functions let you make Ajax a part of your website</p>
    </li>
    <li>
        Plugins
        <p>There are thousands of pre-made, ready-to-roll plugins available for jQuery.  Check out <a href="http://www.unheap.com/">this list</a> for some examples</p>
    </li>
</ul>